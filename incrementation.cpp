#include <iostream> 
using namespace std;

int main() {
	int x = 2, y = 10;
	double d = 4, e = 2;
	cout << "x = " << x << " y = " 
		<< y << " d = " << d << " e = "
		<< e << endl;
	cout << "--x = " << --x << endl;
	cout << "x = " << x << endl;
	cout << "x-- = " << x-- << endl;
	cout << "x = " << x << endl;
	cout << "++y = " << ++y << endl;
	cout << "y = " << y << endl;
	cout << "y++ = " << y++ << endl;
	cout << "y = " << y << endl;
	cout << "x/y = " << x/y << endl;
	cout << "y/e = " << y/e << endl;
	return 0;
}
